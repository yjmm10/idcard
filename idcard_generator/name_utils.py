    def address(self):
        self.pos = self.fake.address().split(' ')[0]
        return self.pos
    
    def sex(self):
        self.male = "男" if self.pt_profile['sex'] =='M' else "女"
        return self.male
    
    def age(self):
        return self.fake.pyint(15,80)
    
    def id(self):
        self.id_num = self.fake.ssn()
        return self.id_num

